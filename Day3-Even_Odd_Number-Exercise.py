# If Else Statement
# Write a program that works out whether if a number is even or odd.
print("Welcome here to check for even or odd number")
number = int(input("Which number you want to check?"))
# Using modulo (Percentage sign in python) to generate reminder.
if number % 2 == 0:
    print("The number is an even number.")
else:
    print("The number is an odd number.")
